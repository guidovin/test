    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String erros = "";
        if (id_instituicao == -1)  erros += "Instituição não foi carregada\n";
        
        if (datePicker.getDate() == null) erros += "Data não selecionada\n";
        
        if (tblPedidos.getSelectedRow() == -1) erros += "Nenhum pedido selecionado\n";   
        
        if (txtFornecedor.getText().isEmpty() || txtFornecedor.getText().length() >= 80) erros += "Fornecedor inválido";
        
        if (!erros.equals("")){
            Auxiliar.errMsg(this, erros, true);
            return;
        }
        
        Entrega e = new Entrega();
        e.setId_pedido((int)tblPedidos.getValueAt(tblPedidos.getSelectedRow(), 0));
        Format formatter = new SimpleDateFormat("dd/MM/yyyy");
        e.setData_entrega(formatter.format(datePicker.getDate()));
        e.setFornecedor(txtFornecedor.getText());
        if (e.cadastrar(id_instituicao)){
            Auxiliar.msg(this, "Entrega cadastrada com sucesso", true);
        } else {
            Auxiliar.errMsg(this, "Erro no BD", true);            
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

