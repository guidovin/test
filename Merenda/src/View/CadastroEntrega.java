    private void btnPesquisaInstituicaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaInstituicaoActionPerformed
        String txt = txtInstituicao.getText();
        if (txt.equals("")){
            Auxiliar.errMsg(this, "Campo \'Instituicao\' vazio.", true);
            return;
        }

        // SE FOR NÚMERO, PROCURA POR ID, SENÃO PESQUISA POR NOME
        Instituicao i = new Instituicao();
        boolean sucesso = Auxiliar.isNumeric(txt) ? i.instituicaoPorId(Integer.parseInt(txt)) : i.instituicaoPorNome(txt);
        if (sucesso){
            id_instituicao = i.getId();
            java.net.URL imgURL = getClass().getResource("/images/success.png");
            ImageIcon icon = Auxiliar.createImageIcon(imgURL, "Instituicão encontrada");
            lblPesquisaInstituicao.setIcon(icon);
            txtInstituicao.setEnabled(false);
            btnPesquisaInstituicao.setEnabled(false);
            txtInstituicao.setText(i.getNome());
            
            // Popula a tabela pedidos            
            List<Map<String,Object>> lst = i.pedidosDaInstituicao();
            DefaultTableModel model = (DefaultTableModel)tblPedidos.getModel();       
            model.setRowCount(0);
            for (int j = 0; j < lst.size(); j++){
                Object row[] = {lst.get(j).get("id"), lst.get(j).get("data")};
                model.addRow(row);            
            }    
            
            // Limpa a tabela de itens
            model = (DefaultTableModel)tblItens.getModel();   
            model.setRowCount(0);
            
        } else {
            Auxiliar.errMsg(this, "Nenhuma instituição encontrada", true);
            java.net.URL imgURL = getClass().getResource("/images/failure.png");
            ImageIcon icon = Auxiliar.createImageIcon(imgURL, "Instituição não encontrada");
            lblPesquisaInstituicao.setIcon(icon);
        }
    }//GEN-LAST:event_btnPesquisaInstituicaoActionPerformed

    private void btnCarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarregarActionPerformed
        if (tblPedidos.getSelectedRow() == -1){
            Auxiliar.errMsg(this, "Nenhum pedido selecionado", true);
            return;
        }
        
        int id_pedido = Integer.parseInt(String.valueOf(tblPedidos.getValueAt(tblPedidos.getSelectedRow(), 0)));
        Pedido p = new Pedido();
        p.setId(id_pedido);
        List<Map<String,Object>> lst = p.consultaLotes();
        DefaultTableModel model = (DefaultTableModel)tblItens.getModel();       
        model.setRowCount(0);
        for (int i = 0; i < lst.size(); i++){
            Object row[] = {lst.get(i).get("alimento"), lst.get(i).get("qtd_alimento")};
            model.addRow(row);            
        }     
    }//GEN-LAST:event_btnCarregarActionPerformed

