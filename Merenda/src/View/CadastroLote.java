    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        /* proof of concept
        if (alimento.getId_alimento() == 0) {
            JOptionPane.showMessageDialog(this, "Alimento ainda n√£o foi carregado", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        */

        Lote l = new Lote();
        String nome, qtd, data, preco;
        nome = txtAlimento.getText();
        qtd = txtQtdAlimento.getText();
        //
        try {
            data = new SimpleDateFormat("dd/MM/yyyy").format(datePicker.getDate());
        } catch(NullPointerException ex) {
            data = "";
        }
        preco = txtPreco.getText();
       
        if (l.pesquisar_do_cadastro_lote(nome, qtd, data, preco)) {
            pode_editar = true;
            
            ModeloLote ml = l.get_ModeloLote();
            Alimento a = new Alimento();
            a.consultar_por_id(ml.get_id_alimento());
            
            txtAlimento.setText(a.getNome());
            txtAlimento.setEnabled(false);
            btnPesquisaAlimento.setEnabled(false);
            
            txtQtdAlimento.setText(Integer.toString(ml.get_qtd_alimento()));
            datePicker.setDate(ml.get_data_validade());
            txtPreco.setText(Float.toString(ml.get_preco()));
            
            // passa pro modo de edi√ß√£o
            btnCadastrar.setEnabled(false);
            btnDeletar.setEnabled(true);
            btnEditar.setEnabled(true);
            btnPesquisar.setEnabled(false);
            this.lote = l;
            this.alimento = a;
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum lote encontrado", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

