    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String nome = txtNome.getText();
        String INEP = txtINEP.getText();
        String telefone = txtTelefone.getText();
        String qntAlunos = txtQntAlunos.getText();
        String diretor = txtDiretor.getText();
        String endereco = txtEndereco.getText();
        
        boolean tem_erro = false;
        String erros = "";
        
        if (nome.length() >= 255 || nome.length() == 0){
            tem_erro = true;
            erros += "Nome invalido\n";              
        }
        // testa se é uma string com apenas digitos de tamanho 11
        if (!Auxiliar.isNumeric(INEP) || INEP.length() != 8){
            tem_erro = true;
            erros += "INEP invalido\n";
        }
        if (!Auxiliar.isNumeric(telefone) || (telefone.length() != 11 && telefone.length() != 10)){
            tem_erro = true;
            erros += "Telefone invalido\n";            
        }
        if (endereco.length() >= 255 || endereco.length() == 0){
            tem_erro = true;
            erros += "Endereco invalido\n";              
        }
        
        if (!Auxiliar.isNumeric(qntAlunos) || Integer.valueOf(qntAlunos) <= 0){
            tem_erro = true;
            erros += "Quantidade de alunos invalida\n";
        }
        
        if (cmbTipo.getSelectedIndex() == -1){
            tem_erro = true;
            erros += "Selecione um tipo de escola\n";            
        }
        
        // Vê se o diretor foi marcado ou não
        // Se tiver -1, não vai inserir
        if (chkDiretor.isSelected()){
            idDiretor = -1;            
        } else {
            if (idDiretor == -1){
                tem_erro = true;
                erros += "É necessário pesquisar o ID ou Nome do diretor antes de cadastrá-lo.\n";
            // SE O CARA DER ALGUM JEITO DE TROCAR O TXT DO DIRETOR MESMO DEPOIS DE TER SETADO ENABLED FALSE
            } else if (!diretor.equals(oldTxtDiretor)) {
                tem_erro = true;
                erros += "Valor inserido em diretor em pesquisa e atual não coincidem.\n";                
            }
        }        
        
        int tipo = (int)inst.get(cmbTipo.getSelectedIndex()).get("id");
        
        if (tem_erro){
            //mensagem de erro
            JOptionPane.showMessageDialog(this, erros, "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            // executa a query
            Conexao con = new Conexao();
            if (con.cadastra_instituicao(nome, endereco, INEP, qntAlunos, telefone, idDiretor, tipo)){
                JOptionPane.showMessageDialog(this, "Cadastro efetuado com sucesso");
            } else {
                JOptionPane.showMessageDialog(this, "Erro no BD", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

