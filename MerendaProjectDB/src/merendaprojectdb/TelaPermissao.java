    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Object type = campoTipo.getSelectedItem();
        tipo = ""+type;
        
        boolean newuser = canNewUser.isSelected();
        boolean getrp = canChangeReport.isSelected();
        boolean seerp = canGetReport.isSelected();
        boolean newrp = canNewReport.isSelected();
        boolean newsc = canNewSchool.isSelected();
        boolean seesc = canGetSchool.isSelected();
        boolean newpm = canChangePerm.isSelected();
        
        
        if(tipo=="Administrador"){
           // UsuarioAdm adm;
            adm.setNR(newrp);
            adm.setWR(getrp);
            adm.setSR(seerp);
            adm.setWS(newsc);
            adm.setSS(seesc);
            adm.setWP(newpm);
        }
        
        if(tipo=="Diretor"){
           // UsuarioDir dir;
            dir.setNR(newrp);
            dir.setWR(getrp);
            dir.setSR(seerp);
            dir.setWS(newsc);
            dir.setSS(seesc);
            dir.setWP(newpm);
        }
        
        if(tipo=="Leitor"){
         //   UsuarioLei lei;
            lei.setNR(newrp);
            lei.setWR(getrp);
            lei.setSR(seerp);
            lei.setWS(newsc);
            lei.setSS(seesc);
            lei.setWP(newpm);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

