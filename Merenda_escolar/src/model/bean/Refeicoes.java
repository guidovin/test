public class Refeicoes {
    
    private int Id_Refeicoes;
    private String Desjejum;
    private String Almoco;
    private String Sobremesa;
    private String Mais;

    public int getId_Refeicoes() {
        return Id_Refeicoes;
    }

    public void setId_Refeicoes(int Id_Refeicoes) {
        this.Id_Refeicoes = Id_Refeicoes;
    }

    public String getDesjejum() {
        return Desjejum;
    }

    public void setDesjejum(String Desjejum) {
        this.Desjejum = Desjejum;
    }

    public String getAlmoco() {
        return Almoco;
    }

    public void setAlmoco(String Almoco) {
        this.Almoco = Almoco;
    }

    public String getSobremesa() {
        return Sobremesa;
    }

    public void setSobremesa(String Sobremesa) {
        this.Sobremesa = Sobremesa;
    }

    public String getMais() {
        return Mais;
    }

    public void setMais(String Mais) {
        this.Mais = Mais;
    }
    
    
    
}
